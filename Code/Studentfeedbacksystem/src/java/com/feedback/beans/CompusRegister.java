public class CompusRegister {
    private String f_username;
    private String f_password;
    private String f_cwid;
    private String f_pin;
    private String f_email;

    public String getF_username() {
        return f_username;
    }

    public void setF_username(String f_username) {
        this.f_username = f_username;
    }

    public String getF_password() {
        return f_password;
    }

    public void setF_password(String f_password) {
        this.f_password = f_password;
    }

    public String getF_cwid() {
        return f_cwid;
    }

    public void setF_cwid(String f_cwid) {
        this.f_cwid = f_cwid;
    }

    public String getF_pin() {
        return f_pin;
    }

    public void setF_pin(String f_pin) {
        this.f_pin = f_pin;
    }

    public String getF_email() {
        return f_email;
    }

    public void setF_email(String f_email) {
        this.f_email = f_email;
    }
    
}
